# Price

def price(people):
    print("{0} people, price is {1}.".format(people, people*1000))

# Morning discount
def price_morning(people):
    print("{0} people, morning discount price is {1}".format(people, people * 500))

# Soldier Discount
def price_soldier(people):
    print("{0} people, Soldier discount price is {1}".format(people, people * 100))

